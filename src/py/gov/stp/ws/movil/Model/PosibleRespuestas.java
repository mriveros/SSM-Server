package py.gov.stp.ws.movil.Model;

public class PosibleRespuestas {
	
	private String id;
	private String texto;
	
	public PosibleRespuestas() {
		super();
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTexto() {
		return texto;
	}

	public void setTexto(String texto) {
		this.texto = texto;
	}
}
